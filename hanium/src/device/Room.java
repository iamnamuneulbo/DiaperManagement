package device;

public class Room {
	private String roomNo;
	private int maxBed;
	
	public String getRoomNo() {
		return roomNo;
	}
	public void setRoomNo(String roomNo) {
		this.roomNo = roomNo;
	}
	public int getMaxBed() {
		return maxBed;
	}
	public void setMaxBed(int maxBed) {
		this.maxBed = maxBed;
	}
}
